import random
print(random.randint(5, 20))   # The smallest number is 5 and the largest is 20.
print(random.randrange(3, 10, 2))   # The smallest number is 3 and the largest is 9.
print(random.uniform(2.5, 5.5))   # The smallest number is 2.5 and the largest number is 5.4.
print(random.randint(1, 100))